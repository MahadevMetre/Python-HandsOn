# Automatic type assignment
x = "Hello World"  # str
y = 20             # int
z = 20.5           # float
c = 1j             # complex
fruits = ["apple", "banana", "cherry"]  # list
info = {"name": "John", "age": 36}      # dict
is_active = True    # bool
b = b"Hello"        # bytes
empty = None        # NoneType

print(type(x))  # Output: <class 'str'>
print(type(y))  # Output: <class 'int'>
print(type(z))  # Output: <class 'float'>
